package loopsDemo;

public class ArrayLoop {

	public static void main(String[] args) {

		// Method-1: Iterate an array using for loop

		int arr[] = { 2, 11, 45, 9 };

		for (int i = 0; i < arr.length; i++) {

			// System.out.println(i); // This will print index values like 0,1,2,3

			System.out.println(arr[i]); // This will print Array values provided above

		}
	}

}
